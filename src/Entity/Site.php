<?php

declare(strict_types=1);

namespace App\Entity;

class Site
{
    /** @var int */
    private $id;
    /** @var string */
    private $url;

    public function getId(): int
    {
        return $this->id;
    }

    public function setId(int $id): self
    {
        $this->id = $id;
        return $this;
    }

    public function getUrl(): string
    {
        return $this->url;
    }

    public function setUrl(string $url): self
    {
        $this->url = $url;
        return $this;
    }
}
